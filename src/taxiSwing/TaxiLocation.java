    private void txtIdtaxiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtIdtaxiActionPerformed
        try {

            int idtaxi = Integer.parseInt(txtIdtaxi.getText());
            List<location> listLoc = taxiDAO.rechLoc(idtaxi);
            int nr = dft1.getRowCount();
            for (int i = nr - 1; i >= 0; i--) {
                dft1.removeRow(i);
            }
            for (location loc : listLoc) {
                Vector v = new Vector();
                v.add(loc.getIdLoc());
                v.add(loc.getDateLoc());
                v.add(loc.getKmtotal());
                v.add(loc.getAcompte());
                v.add(loc.getTotal());
                v.add(loc.getIdClient());
                v.add(loc.getIdTaxi());
                v.add(loc.getIdAdrDebut());
                v.add(loc.getIdAdrFin());
                dft1.addRow(v);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "L'ID est introuvable.", "ERREUR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtIdtaxiActionPerformed


